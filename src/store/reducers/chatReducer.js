const LOADING = "LOADING";
const ACTIVE_CHAT = "ACTIVE_CHAT";


export const chatReducer = (state = defaultState, action) => {
    switch(action.type) {
        case LOADING: 
            return {...state, loading: action.payload};
        case ACTIVE_CHAT: 
            return {...state, activeChatId: action.payload};
            default: 
                return state
    }
}

export const changeActiveChat = (payload) => ({type: ACTIVE_CHAT, payload});
export const setLoadingChat = (payload) => ({type: LOADING, payload});